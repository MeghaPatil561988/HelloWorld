
public class ModularDivision {

	public static void main(String[] args) {
		int x = 46;
        double y = 46.25;
        
        System.out.println("46 div 10 = " + x/10);
        System.out.println("46.25 div 10 = " + x/10);
        System.out.println("46 mod 10 = " + x%10);
        System.out.println("46.25 mod 10 = " +y%10);

	}

}
